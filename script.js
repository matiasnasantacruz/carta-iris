    handleKeyboard(e) {
        // Don't interfere when modal is open
        if (document.getElementById('settings-modal').classList.contains('active')) return;

        switch (e.key) {
            case 'ArrowRight':
            case ' ':
            case 'Enter':
                e.preventDefault();
                this.goToNextChapter();
                break;
            case 'ArrowLeft':
            case 'Backspace':
                e.preventDefault();
                this.goToPreviousChapter();
                break;
            case 'Home':
                e.preventDefault();
                this.goToChapter(1);
                break;
            case 'End':
                e.preventDefault();
                this.goToChapter(7);
                break;
            case 'Escape':
                if (this.isTypewriting) {
                    this.skipTypewriter();
                }
                break;
        }
    }

